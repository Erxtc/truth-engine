// ── Public API ─────────────────────────────────────────────────────────────────

/**
 * Look up reference data for a known algorithm.
 *
 * @param algorithmHint - Text to search for algorithm names in (problem description,
 *   domain name, or domain type). Case-insensitive.
 * @returns ReferenceData if a known algorithm is detected, undefined otherwise.
 */
export function lookupReferenceData(algorithmHint: string): ReferenceData | undefined {
  const lower = algorithmHint.toLowerCase();
  for (const [key, data] of Object.entries(REFERENCE_DATA)) {
    if (lower.includes(key)) {
      return data;
    }
  }
  return undefined;
}

/**
 * Format reference data for injection into the task-agent prompt.
 * Returns an empty string if no reference data matches.
 */
export function formatReferenceData(hint: string): string {
  const data = lookupReferenceData(hint);
  if (!data || !data.content) return "";

  return `\n\n── REFERENCE DATA: ${data.label} ──\nThe following canonical values are provided so you don't need to recall or web-search them. Trust these values — they are the official specification:\n\n${data.content}`;
}
