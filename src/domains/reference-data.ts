/**
 * Reference data injection for standard algorithms.
 *
 * When a problem matches a known algorithm (AES, RSA, SHA, etc.), this module
 * provides canonical constants, lookup tables, and pseudocode so the model
 * doesn't need to recall them from memory or hallucinate them. The model's job
 * shifts from "know the algorithm" to "implement known reference data correctly."
 *
 * Injected into the task-agent prompt via the same mechanism as oracleContent.
 */

// ── Types ──────────────────────────────────────────────────────────────────────

export interface ReferenceData {
  /** Human-readable label shown to the model */
  label: string;
  /** The reference content injected into the prompt */
  content: string;
}

